#include<iostream>
using namespace std;
int main()
{
	char c='a';
	short s=-1;
	int i=330014;
	bool b=true;
	long l=23894634;
	float f=1.92f;
	long double ld=78.8956;
	wchar_t wc=L'a';
	cout<<"Character variable	:"<<c<<endl;
	cout<<"Short variable		:"<<s<<endl;
	cout<<"Integer variable		:"<<i<<endl;
	cout<<"Bool variable		:"<<b<<endl;
	cout<<"Long variable		:"<<l<<endl;
	cout<<"Float variable		:"<<f<<endl;
	cout<<"Long Double variable	:"<<ld<<endl;
	cout<<"Wide Character variable	:"<<wc<<endl;
return 0;
}
